	into
	public.beatmaplive
(beatmap_id,
	user_id,
	beatmapset_id,
	"mode",
	status,
	stars,
	od,
	ar,
	bpm,
	cs,
	hp,
	length,
	drain_time,
	count_circles,
	count_sliders,
	count_spinners,
	max_combo,
	pass_count,
