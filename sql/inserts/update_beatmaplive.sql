	b.beatmapset_ranked_date,
	b.beatmapset_submitted_date,
	b.beatmapset_last_updated,
	b."version",
	b.beatmapset_title,
	b.beatmapset_artist,
	b.beatmapset_source,
	b.beatmapset_tags,
