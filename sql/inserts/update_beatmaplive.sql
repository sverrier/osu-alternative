	ranked_date,
	submitted_date,
	last_updated,
	"version",
	title,
	artist,
	"source",
	tags,
	checksum)
select b.id,
	b.user_id,
	b.beatmapset_id,
	b.mode_int,
	b.status,
	b.difficulty_rating ,
	b.accuracy,
	b.ar,
	b.bpm,
	b.cs,
